        void balanc(double** a, int n)
        {
            int last, j, i;
            double s,r,g,f,c,sqrdx;

            sqrdx = (double)RADIX*RADIX;
            last = 0;

            while( last == 0 )
            {
                last = 1;
                for(i = 1; i <= n; i++)
                {
                    r = 0.0;
                    c = 0.0;
                    for(j = 1; j <= n; j++)
                    {
                        if( j != i )
                        {
                            c += fabs(a[j][i]);
                            r += fabs(a[i][j]);
                        }
                    }

                    if( c && r )
                    {
                        g = r/RADIX;
                        f = 1.0;
                        s = c+r;
                        while( c < g )
                        {
                            f *= RADIX;
                            c *= sqrdx;
                        }

                        g = r*RADIX;
                        while( c > g )
                        {
                            f /= RADIX;
                            c /= sqrdx;
                        }

                        if( (c+r)/f < .95*s )
                        {
                            last = 0;
                            g = 1.0/f;
                            for(j = 1; j <= n; j++)
                            {
                                a[i][j] *= g;
                            }
                            for(j = 1; j <=n; j++)
                            {
                                a[j][i] *= f;
                            }
                        }
                    }
                }
            }
        }

        void hqr(double **a, int n, double* wr, double* wi)
        {
            //cout << "Into hqr." << endl;
            //printMatrix(a, n);

            int nn,m,l,k,j,its,i,mmin;
            double z,y,x,w,v,u,t,s,anorm;
            double r = 0.0;
            double p = 0.0;
            double q = 0.0;
            //int totalIts = 0;
            anorm=0.0; //Compute matrix norm for possible use in locating
            //single small subdiagonal element.
            for (i=1;i<=n;i++)
            {
                for (j=std::max(i-1,1);j<=n;j++)
                {
                    anorm += fabs(a[i][j]);
                }
            }

            nn=n;
            t=0.0; //Gets changed only by an exceptional shift.
            while (nn >= 1) //Begin search for next eigenvalue.
            {
                its=0;
                do
                {
                    for (l=nn;l>=2;l--)
                    {
                        //Begin iteration: look for single small subdiagonal
                        //element.
                        s=fabs(a[l-1][l-1])+fabs(a[l][l]);
                        if (s == 0.0) s=anorm;
                        if ((double)(fabs(a[l][l-1]) + s) == s)
                        {
                            a[l][l-1]=0.0;
                            break;
                        }
                    }
                    x=a[nn][nn];
                    if (l == nn)
                    {
                        //One root found.
                        //wr[nn]=x+t;
                        wr[nn-1]=x+t;
                        //wi[nn--]=0.0;
                        wi[nn-1] = 0.0;
                        nn--;
                    }
                    else
                    {
                        y=a[nn-1][nn-1];
                        w=a[nn][nn-1]*a[nn-1][nn];
                        if (l == (nn-1))
                        { //Two roots found...

                            p=0.5*(y-x);
                            q=p*p+w;
                            z=sqrt(fabs(q));
                            x += t;
                            if (q >= 0.0)
                            { //...a real pair.
                                z=p+SIGN(z,p);
                                //wr[nn-1]=wr[nn]=x+z;
                                wr[nn-2]=wr[nn-1]=x+z;
                                //if (z) wr[nn]=x-w/z;
                                if (z) wr[nn-1]=x-w/z;
                                //wi[nn-1]=wi[nn]=0.0;
                                wi[nn-2]=wi[nn-1]=0.0;
                            }
                            else
                            { //...a complex pair.
                                //wr[nn-1]=wr[nn]=x+p;
                                wr[nn-2]=wr[nn-1]=x+p;
                                //wi[nn-1]= -(wi[nn]=z);
                                wi[nn-2]= -(wi[nn-1]=z);
                            }

                            nn -= 2;
                        }
                        else
                        { //No roots found. Continue iteration.
                            //cout << "Iteration " << its << endl;
                            //printMatrix(a, n);
                            //if (its == 30) cerr << "Too many iterations in hqr" << endl;
                            //if( its > 30 )
                            //{
                            //  cerr << its << endl;
                            //}
                            if (its == 10 || its == 20)
                            { //Form exceptional shift.
                                t += x;
                                for (i=1;i<=nn;i++) a[i][i] -= x;
                                s=fabs(a[nn][nn-1])+fabs(a[nn-1][nn-2]);
                                y=x=0.75*s;
                                w = -0.4375*s*s;
                            }
                            ++its;
                            //totalIts++;
                            for (m=(nn-2);m>=l;m--)
                            { //Form shift and then look for
                                //2 consecutive small subdiagonal
                                //elements.
                                z=a[m][m];
                                r=x-z;
                                s=y-z;
                                p=(r*s-w)/a[m+1][m]+a[m][m+1]; //Equation (11.6.23).
                                q=a[m+1][m+1]-z-r-s;
                                r=a[m+2][m+1];
                                s=fabs(p)+fabs(q)+fabs(r); //Scale to prevent overflow or
                                // underflow.
                                p /= s;
                                q /= s;
                                r /= s;
                                if (m == l) break;

                                u=fabs(a[m][m-1])*(fabs(q)+fabs(r));
                                v=fabs(p)*(fabs(a[m-1][m-1])+fabs(z)+fabs(a[m+1][m+1]));
                                if ((double)(u+v) == v) break; //Equation (11.6.26).
                            }
                            for (i=m+2;i<=nn;i++)
                            {
                                a[i][i-2]=0.0;
                                if (i != (m+2)) a[i][i-3]=0.0;
                            }
                            for (k=m;k<=nn-1;k++)
                            {
                                //Double QR step on rows l to nn and columns m to nn.
                                if (k != m)
                                {
                                    p=a[k][k-1]; //Begin setup of Householder vector.
                                    q=a[k+1][k-1];
                                    r=0.0;
                                    if (k != (nn-1)) r=a[k+2][k-1];
                                    if ((x=fabs(p)+fabs(q)+fabs(r)) != 0.0)
                                    {
                                        p /= x; //Scale to prevent overflow or
                                        //underflow.
                                        q /= x;
                                        r /= x;
                                    }
                                }
                                if ((s=SIGN(sqrt(p*p+q*q+r*r),p)) != 0.0)
                                {
                                    if (k == m)
                                    {
                                        if (l != m)
                                        {
                                            a[k][k-1] = -a[k][k-1];
                                        }
                                    }
                                    else
                                    {
                                        a[k][k-1] = -s*x;
                                    }
                                    p += s; //Equations (11.6.24).
                                    x=p/s;
                                    y=q/s;
                                    z=r/s;
                                    q /= p;
                                    r /= p;
                                    for (j=k;j<=nn;j++)
                                    { //Row modification.
                                        p=a[k][j]+q*a[k+1][j];
                                        if (k != (nn-1))
                                        {
                                            p += r*a[k+2][j];
                                            a[k+2][j] -= p*z;
                                        }
                                        a[k+1][j] -= p*y;
                                        a[k][j] -= p*x;
                                    }
                                    mmin = nn<k+3 ? nn : k+3;
                                    for (i=l;i<=mmin;i++)
                                    { //Column modification.
                                        p=x*a[i][k]+y*a[i][k+1];
                                        if (k != (nn-1)) {
                                            p += z*a[i][k+2];
                                            a[i][k+2] -= p*r;
                                        }
                                        a[i][k+1] -= p*q;
                                        a[i][k] -= p;
                                    }
                                }
                            }
                        }
                    }
                } while (l < nn-1);
                //cout << its << endl;
            }
        }

